package com.shopoline.xingyuanji.model;

public class EveryDaySellAmountModel {

    private String days;

    private String amount;

    private String ZIPAmount;

    public String getDays() {
        return days;
    }

    public void setDays(String days) {
        this.days = days;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getZIPAmount() {
        return ZIPAmount;
    }

    public void setZIPAmount(String ZIPAmount) {
        this.ZIPAmount = ZIPAmount;
    }
}
