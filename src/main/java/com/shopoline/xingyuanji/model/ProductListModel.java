package com.shopoline.xingyuanji.model;

public class ProductListModel {

    private String productId;

    private String productName;

    private String price;

    private String productImg;

    private String productShopImg;

    private String productNum;

    private String productStyle;

    private String productSocer;

    private String editTime;

    private String productStatus;

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getProductImg() {
        return productImg;
    }

    public void setProductImg(String productImg) {
        this.productImg = productImg;
    }

    public String getProductNum() {
        return productNum;
    }

    public void setProductNum(String productNum) {
        this.productNum = productNum;
    }

    public String getProductStyle() {
        return productStyle;
    }

    public void setProductStyle(String productStyle) {
        this.productStyle = productStyle;
    }

    public String getProductSocer() {
        return productSocer;
    }

    public void setProductSocer(String productSocer) {
        this.productSocer = productSocer;
    }

    public String getEditTime() {
        return editTime;
    }

    public void setEditTime(String editTime) {
        this.editTime = editTime;
    }

    public String getProductStatus() {
        return productStatus;
    }

    public void setProductStatus(String productStatus) {
        this.productStatus = productStatus;
    }

    public String getProductShopImg() {
        return productShopImg;
    }

    public void setProductShopImg(String productShopImg) {
        this.productShopImg = productShopImg;
    }
}
